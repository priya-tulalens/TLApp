public class MainActivity extends ActionBarActivity implements View.OnClickListener {
    Button optionsButton;
    Button surveyMenuButton;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        ParseAnalytics.trackAppOpenedInBackground(getIntent());

        this.optionsButton = (Button) findViewById(R.id.go_to_options_button);
        this.surveyMenuButton = (Button) findViewById(R.id.go_to_surveys_menu_button);
        this.optionsButton.setOnClickListener(this);
        this.surveyMenuButton.setOnClickListener(this);
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.menu_main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }

    @Override
    public void onClick(View v) {
        Intent intent;
        switch (v.getId()) {
            case R.id.go_to_options_button:
                intent = new Intent(this, OptionsActivity.class);
                startActivity(intent);
                finish();
                break;
            case R.id.go_to_surveys_menu_button:
                intent = new Intent(this, SurveyMenuActivity.class);
                startActivity(intent);
                finish();
                break;
            default:
                break;
        }
    }
}